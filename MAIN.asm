      MOV BX, UPPERBOUND_Y
      SUB CX, BX
      CMP CX, 3  ;CURRENT Y DISTANCE TO THE BOUNDARY VS REMAINING DISTANCE TO THE BOUNDARY
      JL BACKTOMAINLOOP
      SUB STARTPOS_Y_PLAYER2, 3
      JMP BACKTOMAINLOOP   
   ;}
   
   MOVEDOWN2:
