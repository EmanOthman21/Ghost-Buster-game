      SUB ORIENTATION_PLAYER1, 1 
      JMP BACKTOMAINLOOP_1
   ;}
   ORIENT_DOWN1:
   ;{
      CMP AL, 'e' 
      JNE MOVEUP2
      CMP ORIENTATION_PLAYER1, 4
      JE BACKTOMAINLOOP
      ADD ORIENTATION_PLAYER1, 1 
      BACKTOMAINLOOP_1:      ;JUST A MIDDLE JUMP TO USE IT FOR THE PREVIOUS LABELS
      JMP BACKTOMAINLOOP         
   ;}

    MOVEUP2:
   ;{
      CMP AL, 'i' 
      JNE MOVEDOWN2
