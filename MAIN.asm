DRAW_GHOST1 PROC
;{
   PUSH AX
   PUSH BX
   PUSH CX
   PUSH DX

   CMP EXISTS_GHOST1, 1
   JNE ENDDRAW_GHOST11
   DOWNRIGHT_GHOST1:
   ;{ DOWNRIGHT MOVEMENT
      CMP MOTION_GHOST1, 0
      JNE DOWNLEFT_GHOST1
      ADD STARTPOS_X_GHOST1, 8
      ADD STARTPOS_Y_GHOST1, 8
      JMP CONTINUE_DRAW1
   ;}
   DOWNLEFT_GHOST1:
   ;{ DOWNLEFT MOVEMENT
      CMP MOTION_GHOST1, 1
      JNE UPRIGHT_GHOST1
      DEC STARTPOS_X_GHOST1, 8
      ADD STARTPOS_Y_GHOST1, 8
      JMP CONTINUE_DRAW1
   ;}
   UPRIGHT_GHOST1:
   ;{ UPRIGHT MOVEMENT
      CMP MOTION_GHOST1, 2
      JNE UPLEFT_GHOST1
      ADD STARTPOS_X_GHOST1, 8
      DEC STARTPOS_Y_GHOST1, 8
      JMP CONTINUE_DRAW1
   ;}
   UPLEFT_GHOST1:
   ;{ UPLEFT MOVEMENT
      CMP MOTION_GHOST1, 3
   ENDDRAW_GHOST11:   
      JNE ENDDRAW_GHOST12
      DEC STARTPOS_X_GHOST1, 8
      DEC STARTPOS_Y_GHOST1, 8
      JMP CONTINUE_DRAW1
   ;}
   
   CONTINUE_DRAW1:
   WITHIN_BOUNDARIES STARTPOS_X_GHOST1, STARTPOS_Y_GHOST1, GHOSTSIZE , EXISTS_GHOST1 ;CHECK WHETHER WITHIN BOUNDARIES
   CMP EXISTS_GHOST1, 1
   ENDDRAW_GHOST12:     ; JUST A MIDDLE LABEL FOR JUMP
   JNE ENDDRAW_GHOST1
   MOV SI, OFFSET GHOST1
   DRAW_OBJECT GHOSTSIZE, SI, STARTPOS_X_GHOST1, STARTPOS_Y_GHOST1
   
   ENDDRAW_GHOST1:
   POP DX
   POP CX
   POP BX
   POP AX 
   RET
;} 
DRAW_GHOST1 ENDP


