   PUSH BX
   PUSH CX
   PUSH DX

   MOV EXISTS_GHOST2, 1
   ;RANDOMIZE 0, 3 , MOTION_GHOST2
   MOV MOTION_GHOST2, 1    ;MOVE BY INC Y AND DEC X
   RANDOMIZE 0, 2, POWERUP_GHOST2
   
   MOV AX, UPPERBOUND_Y
   ADD AX, 20
   MOV LOWER4MACRO, AX
   MOV BX, UPPERBOUND_Y 
   ADD BX, 280
   MOV UPPER4MACRO, BX
   RANDOMIZE  LOWER4MACRO, UPPER4MACRO, STARTPOS_Y_GHOST2

   MOV AX, RIGHTBOUND_X
   SUB AX, 280
   MOV LOWER4MACRO, AX
   MOV BX, RIGHTBOUND_X 
   SUB BX, 120
   MOV UPPER4MACRO, BX
   RANDOMIZE LOWER4MACRO, UPPER4MACRO, STARTPOS_X_GHOST2
   
   POP DX
   POP CX
   POP BX
   POP AX 
   RET
;} 
RANDOMIZE_GHOST2 ENDP

