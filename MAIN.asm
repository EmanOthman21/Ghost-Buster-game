   PUSH AX
   PUSH BX
   PUSH CX
   PUSH DX

   MOV AH, 0
   INT 16H     ; TAKE THE USER INPUT FROM THE KEYBOARD BUFFER 
   CALL CLEARKEYBOARDBUFFER  
   
   
   MOVEUP1:
   ;{
      CMP AL, 'w' 
